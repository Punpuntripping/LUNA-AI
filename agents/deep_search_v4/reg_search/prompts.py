def build_reranker_user_message(
    query: str,
    rationale: str,
    results_markdown: str,
    round_num: int = 1,
    *,
    max_high: int = 0,
    max_medium: int = 0,
) -> str:
    """Build the user message for one reranker classification run.

    Args:
        query: The expanded sub-query text.
        rationale: Expander's rationale for this query.
        results_markdown: Search results markdown (raw or re-assembled after unfold).
        round_num: Which classification round (1=initial, 2+=after unfold).
        max_high: If nonzero, inject a cap instruction into the prompt.
        max_medium: If nonzero, inject a cap instruction into the prompt.
    """
    lines: list[str] = [
        "## الاستعلام الفرعي",
        query,
    ]
    if rationale:
        lines.append(f"**المبرر:** {rationale}")
    lines.append("")

    if round_num > 1:
        lines.append(f"**الجولة {round_num}:** النتائج أدناه بعد التوسع — أعد التصنيف.")
        lines.append("")

    if max_high > 0 and max_medium > 0:
        lines.append(
            f"**تعليمات الحد الأقصى:** احتفظ بحد أقصى {max_high} نتيجة عالية الصلة "
            f"و{max_medium} نتيجة متوسطة الصلة لهذا الاستعلام الفرعي."
        )
        lines.append("")

    lines.append("---")
    lines.append("")
    lines.append("## نتائج البحث")
    lines.append("")
    lines.append(results_markdown)
    return "\n".join(lines)
