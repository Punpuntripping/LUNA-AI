PROMPTS: OrderedDict[str, str] = OrderedDict([
    ("default", _DEFAULT),
    ("concise", _CONCISE),
    ("thorough", _THOROUGH),
    ("cross_feed", _CROSS_FEED),
])

DEFAULT_PROMPT_NAME = next(iter(PROMPTS))


def get_prompt(name: str | None = None) -> str:
    """Return the named prompt, or the default if name is None."""
    if name is None:
        return PROMPTS[DEFAULT_PROMPT_NAME]
    if name not in PROMPTS:
        available = ", ".join(PROMPTS.keys())
        raise ValueError(f"Unknown prompt '{name}'. Available: {available}")
    return PROMPTS[name]


def list_prompts() -> list[tuple[str, str]]:
    """Return list of (name, first_line_preview) for all prompts."""
    result = []
    for name, text in PROMPTS.items():
        preview = text.strip().split("\n")[0][:80]
        is_default = " (default)" if name == DEFAULT_PROMPT_NAME else ""
        result.append((f"{name}{is_default}", preview))
    return result
