# -- Dynamic Instruction Builders -----------------------------------------------


def build_plan_agent_dynamic_instructions(
    deps: DeepSearchDeps,
    task_history_formatted: str | None,
    loop_results: list[LoopResult],
) -> str:
    """Build dynamic instructions for PlanAgent.

    Injected per-call. Includes: case memory, previous report, task history,
    loop results from completed invocations.
    """
    sections: list[str] = []

    # Case memory context
    if deps._case_memory:
        sections.append(
            "---\n"
            "سياق القضية (من ذاكرة القضية):\n"
            f"{deps._case_memory}\n"
            "استخدم هذا السياق لتوجيه تحليلك. إذا تضمّنت القضية أنظمة أو مجالات "
            "قانونية محددة، أعطها أولوية في استعلاماتك."
        )

    # Previous report content (when editing)
    if deps._previous_report_md:
        truncated = deps._previous_report_md[:4000]
        if len(deps._previous_report_md) > 4000:
            truncated += "\n... (تم اقتطاع التقرير للاختصار)"
        sections.append(
            "---\n"
            "التقرير السابق (للتعديل والتحسين):\n"
            f"{truncated}"
        )

    # Task history (prior turns in this task)
    if task_history_formatted:
        sections.append(
            "---\n"
            "سجل المحادثة السابقة:\n"
            f"{task_history_formatted}"
        )

    # Loop results (from completed invoke_search_loop calls this turn)
    if loop_results:
        results_sections: list[str] = []
        for i, lr in enumerate(loop_results, 1):
            results_sections.append(format_loop_result_for_plan_agent(lr, index=i))
        sections.append(
            "---\n"
            "نتائج البحث المكتملة:\n"
            + "\n\n".join(results_sections)
        )

    # Artifact ID context
    if deps.artifact_id:
        sections.append(
            "---\n"
            f"معرّف التقرير الحالي: {deps.artifact_id}\n"
            "وضع التعديل مفعّل — استخدم update_report لتحديث التقرير الموجود."
        )

    return "\n\n".join(sections)


def build_expander_dynamic_instructions(
    weak_axes: list[WeakAxis],
    strong_results_summary: str | None = None,
) -> str:
    """Build dynamic instructions for QueryExpander on round 2+.

    Injects weak_axes feedback so the expander generates queries
    ONLY for weak tools.
    """
    sections: list[str] = []

    if weak_axes:
        weak_lines = []
        for wa in weak_axes:
            weak_lines.append(
                f"- أداة: {wa.tool} | السبب: {wa.reason} | "
                f"استعلام مقترح: {wa.suggested_query}"
            )

        sections.append(
            "---\n"
            "إعادة التخطيط — محاور ضعيفة تحتاج إعادة بحث:\n"
            + "\n".join(weak_lines)
        )

        if strong_results_summary:
            sections.append(
                "\nالنتائج القوية المُثبّتة (لا تُعد البحث عنها):\n"
                f"{strong_results_summary}"
            )

        sections.append(
            "\nأنشئ استعلامات فقط للأدوات الضعيفة أعلاه. "
            "لا تُعد استعلامات لأدوات أنتجت نتائج قوية.\n"
            "يمكنك الآن استخدام أسماء أنظمة أو جهات ظهرت في نتائج الجولة السابقة.\n"
            "لا تكرر نفس الاستعلام بصياغة مشابهة."
        )

    return "\n".join(sections)


# -- Search Result Formatters --------------------------------------------------


def format_search_results_for_aggregator(results: list[SearchResult]) -> str:
    """Format search results into markdown for the aggregator's user message.

    Groups results by tool and includes all raw markdown content.
    """
    if not results:
        return "لا توجد نتائج بحث."

    # Group by tool
    by_tool: dict[str, list[SearchResult]] = {}
    for r in results:
        by_tool.setdefault(r.tool, []).append(r)

    sections: list[str] = []

    tool_labels = {
        "regulations": "الأنظمة واللوائح",
        "cases": "السوابق القضائية",
        "compliance": "الخدمات الحكومية",
    }

    for tool, tool_results in by_tool.items():
        label = tool_labels.get(tool, tool)
        mock_tag = " (نتائج تجريبية)" if any(r.is_mock for r in tool_results) else ""
        sections.append(f"## نتائج {label}{mock_tag}\n")

        for i, r in enumerate(tool_results, 1):
            sections.append(
                f"### استعلام {i}: {r.query}\n"
                f"عدد النتائج: {r.result_count}\n\n"
                f"{r.raw_markdown}\n"
            )

    return "\n---\n\n".join(sections)


def build_aggregator_user_message(
    sub_question: str,
    search_results: list[SearchResult],
) -> str:
    """Build the aggregator's user message: sub-question + formatted results."""
    results_md = format_search_results_for_aggregator(search_results)

    return (
        f"## السؤال القانوني\n\n{sub_question}\n\n"
        f"---\n\n"
        f"## نتائج البحث\n\n{results_md}"
    )


def format_loop_result_for_plan_agent(
    loop_result: LoopResult,
    index: int = 1,
) -> str:
    """Format a LoopResult into a readable string for PlanAgent context.

    This is injected as dynamic instruction after each invoke_search_loop.
    """
    lines: list[str] = [
        f"### نتيجة البحث {index}",
        f"السؤال الفرعي: {loop_result.sub_question}",
        f"عدد الجولات: {loop_result.rounds_used}",
    ]

    if loop_result.answer_summary:
        lines.append(f"الملخص: {loop_result.answer_summary}")

    if loop_result.artifact_id:
        lines.append(f"معرّف التقرير: {loop_result.artifact_id}")

    if loop_result.citations:
        lines.append(f"عدد الاستشهادات: {len(loop_result.citations)}")

    return "\n".join(lines)
