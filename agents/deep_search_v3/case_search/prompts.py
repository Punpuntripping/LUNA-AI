def get_expander_prompt(key: str) -> str:
    """Lookup an expander prompt variant by key.

    Sectioned prompts (prompt_3+) have a `{SECTORS}` placeholder that is
    substituted with the canonical case-level sector list at render time so
    the prompt text stays in sync with the DB vocabulary.
    """
    if key not in EXPANDER_PROMPTS:
        available = ", ".join(sorted(EXPANDER_PROMPTS.keys()))
        raise KeyError(f"Expander prompt '{key}' not found. Available: {available}")
    template = EXPANDER_PROMPTS[key]
    if "{SECTORS}" in template:
        # Local import to avoid a circular import at module load time
        from .sector_vocab import SECTORS_PROMPT_LIST
        template = template.replace("{SECTORS}", SECTORS_PROMPT_LIST)
    return template


